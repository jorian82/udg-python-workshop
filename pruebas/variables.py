'''
    Pruebas de variables
'''
print("Ingresa dos numeros: ")
n = int(input("Primer numero: "))
m = int(input("Segundo numero: "))
print("{} + {} = {}".format(n,m,n + m))
print("{} - {} = {}".format(n,m,n - m))
print("{} x {} = {}".format(n,m,n * m))
print("{} / {} = {}".format(n,m,n / m))
print("{} % {} = {}".format(n,m,n % m))
print("{} // {} = {}".format(n,m,n // m))
print("{} ^ {} = {}".format(n,m,n ** m))

